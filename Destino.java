package agenciaViagem1;

public class Destino {
	private String guaruja;
	private String paraty;
	private String trindade;
	
	
	public void setGuaruja(String guaruja) {
		this.guaruja = guaruja;
		
	}
	
	public String getGuaruja() {
		return this.guaruja;
	}
	
	public void setParaty(String paraty) {
		this.paraty = paraty;
		
	}
	
	public String getParaty() {
		return this.paraty;
	}
	
	
	public void setTrindade(String trindade) {
		this.trindade = trindade;
		
	}
	
	public String getTrindade() {
		return this.trindade;
	}

}
